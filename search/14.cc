long long findSubarray(vector<long long>& arr, int n ) {
  auto at = const_accessor(arr);
  unordered_map<long long, long long> sum_map;
  long long sum = 0;
  long long ans = 0;
  for(int i = 0; i < n; i++){
    sum += at(i);
    if(sum == 0){
      ans++;
    }
    sum_map[sum]++;
    ans += sum_map[sum] - 1;
  }
  return ans;
}

