int *findTwoElement(int *arr, int n) {
  auto at = [&](int i) -> int&{
    return arr[i - 1];
  };
  int i = 1;
  int dup;
  while(i <= n){
    if(i == at(i)){
      i++;
      continue;
    }
    int j = at(i);
    if(at(j) == j){
      dup = j;
      break;
    }
    swap(at(i), at(j));
  }
  long long int sum =  0;
  for(int i = 0; i < n; i++){
    sum += (long long int)(arr[i]);
  }
  long long int missing = dup - sum + ((long long int)(n)*((long long int)(n)+1)/2);
  static int ans[2];
  ans[0] = dup;
  ans[1] = missing;
  return ans;
}
