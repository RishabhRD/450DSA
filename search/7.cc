int majorityElement(int a[], int size) {
  int majority_index = 0;
  int majority_count = 1;
  for(int i = 1; i < size; i++){
    if(a[i] == a[majority_index]){
      majority_count++;
    }else{
      majority_count--;
    }
    if(majority_count == 0){
      majority_count = 1;
      majority_index = i;
    }
  }

  int count = 0;
  int majority_ele = a[majority_index];
  for(int i = 0; i < size; i++){
    if(a[i] == majority_ele){
      count++;
    }
  }
  if(count > size/2){
    return majority_ele;
  }else{
    return -1;
  }
}
