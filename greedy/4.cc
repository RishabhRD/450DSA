template <typename T> using lmt = std::numeric_limits<T>;

struct Item {
  int value;
  int weight;
};

double fractionalKnapsack(int w, Item arr[], int n) {
  auto rate = [](const Item& item) -> double{
    return ((double) item.value) / ((double) item.weight);
  };

  sort(arr, arr + n, [&](const auto& a, const auto& b){
      return rate(a) > rate(b);
      });

  double ans = 0;
  int weight_remaining = w;
  int cur_index = 0;
  while(w > 0 and cur_index < n){
    if(weight_remaining >= arr[cur_index].weight){
      ans += arr[cur_index].value;
      weight_remaining -= arr[cur_index].weight;
    }else{
      ans += rate(arr[cur_index]) * weight_remaining;
      weight_remaining = 0;
    }
    cur_index++;
  }
  return ans;
}
