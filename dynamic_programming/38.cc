template <typename T>
using lmt = std::numeric_limits<T>;

struct node{
  node* left;
  node* right;
  int lis = -1;
};

int lis(node* root){
  if(root == nullptr){
    return 0;
  }
  if(root->lis >= 0){
    return root->lis;
  }

  int not_taking_root = lis(root->left) + lis(root->right);

  int taking_root = 1;
  if(root->left){
    taking_root += lis(root->left->left) + lis(root->left->right);
  }
  if(root->right){
    taking_root += lis(root->right->left) + lis(root->right->right);
  }

  root->lis = max(not_taking_root, taking_root);

  return root->lis;
}
