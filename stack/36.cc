string FirstNonRepeating(string A){
  queue<char> q;
  vector<int> char_freq(26);
  string str;
  for(auto c : A){
    q.push(c);
    char_freq[c - 'a']++;
    while(not q.empty()){
      char top = q.front();
      if(char_freq[top - 'a'] > 1){
        q.pop();
      }else{
        str += top;
        break;
      }
    }
    if(q.empty()){
      str += '#';
    }
  }
  return str;
}

