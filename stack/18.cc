bool checkRedundancy(string& str){
  stack<char> st;
  bool last_pop = false;
  bool last_alpha = false;
  for(auto c : str){
    if(c == ')'){
      bool any_char_found = false;
      bool operator_found = false;
      while(!st.empty()){
        char top = st.top();
        if(top == '*' or top == '+' or top == '-' or top == '/'){
          any_char_found = true;
          operator_found = true;
        }else if(top == '('){
          if(any_char_found == false){
            return true;
          }
          if(operator_found == false){
            return true;
          }
        }else{
          any_char_found = true;
        }
        st.pop();
      }
    }else{
      st.push(c);
    }
  }
  return false;
}
