class StackQueue{
private:   
    // These are STL stacks ( http://goo.gl/LxlRZQ )
    stack<int> s1;
    stack<int> s2;
public:
    void push(int);
    int pop();
};

//Function to push an element in queue by using 2 stacks.
void StackQueue :: push(int x) {
  if(s1.empty()){
    s1.push(x);
  }else{
    while(not s1.empty()){
      const int top = s1.top();
      s1.pop();
      s2.push(top);
    }
    s1.push(x);
    while(not s2.empty()){
      const int top = s2.top();
      s2.pop();
      s1.push(top);
    }
  }
}

//Function to pop an element from queue by using 2 stacks.
int StackQueue :: pop() {
  if(s1.empty()) return -1;
  const int top = s1.top();
  s1.pop();
  return top;
}
